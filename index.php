                Mais, il peut également être un objet de médiation, témoin de l'échange "vertical" avec l'art, les institutions et les artistes,<br>
                mais témoin aussi d'échanges "horizontaux" entre visiteurs.<br>
                De ce point de vue, qui n'a jamais ouvert un livre d'or juste pour y lire les commentaires des autres visiteurs ?<br>
                Objet participatif qui entretient un rapport physique avec le visiteur, le livre d'or peut devenir un catalyseur<br>
                entre le lieu, l'oeuvre, l'artiste et le public.
